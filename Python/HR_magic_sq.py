if __name__ == '__main__':

    sq = [
        [[8, 1, 6], [3, 5, 7], [4, 9, 2]],
        [[6, 1, 8], [7, 5, 3], [2, 9, 4]],
        [[4, 9, 2], [3, 5, 7], [8, 1, 6]],
        [[2, 9, 4], [7, 5, 3], [6, 1, 8]],
        [[8, 3, 4], [1, 5, 9], [6, 7, 2]],
        [[4, 3, 8], [9, 5, 1], [2, 7, 6]],
        [[6, 7, 2], [1, 5, 9], [8, 3, 4]],
        [[2, 7, 6], [9, 5, 1], [4, 3, 8]],
    ]

    s = []

    for _ in range(3):
        s.append(list(map(int, input().split())))

    m = 100000

    for k in range(0, 8):
        ctr = 0
        for i in range(0, 3):
            for j in range(0, 3):
                if s[i][j] != sq[k][i][j]:
                    ctr += abs(s[i][j] - sq[k][i][j])

        if ctr <= m:
            m = ctr

    print(m)
